export type TranslationKey = keyof typeof translations.en;

function isLanguage(value: string | null): value is Language {
  return value === 'de' || value === 'en';
}

function readStoredLanguage(): Language {
  if (typeof window === 'undefined') return 'de';

  try {
    const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return isLanguage(storedLanguage) ? storedLanguage : 'de';
  } catch {
    return 'de';
  }
}

function translate(language: Language, key: TranslationKey, values?: TranslationValues) {
  const template: string = translations[language][key] ?? translations.de[key] ?? key;
  if (!values) return template;

  return Object.entries(values).reduce(
    (message, [name, value]) => message.split(`{${name}}`).join(String(value)),
    template,
  );
}

const fallbackContext: LanguageContextValue = {
  language: 'en',
  locale: localeByLanguage.en,
  setLanguage: () => undefined,
  t: (key, values) => translate('en', key, values),
};

const LanguageContext = createContext<LanguageContextValue>(fallbackContext);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => readStoredLanguage());

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = (nextLanguage: Language) => {
    setLanguageState(nextLanguage);
    try {
      window.localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
    } catch {
      // Local storage can be unavailable in private browsing or tests.
    }
  };

  const value = useMemo<LanguageContextValue>(() => ({
    language,
    locale: localeByLanguage[language],
    setLanguage,
    t: (key, values) => translate(language, key, values),
  }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  return useContext(LanguageContext);
}

export function formatAttendanceLabel(
  t: LanguageContextValue['t'],
  input: {
    attendingCount: number;
    maximumAttendees: number | null;
    remainingMinimum: number;
    isMinimumReached: boolean;
    isFull: boolean;
    status: 'draft' | 'open' | 'confirmed' | 'cancelled' | 'archived';
  },
) {
  const spots = input.maximumAttendees
    ? t('attendance.seats', { attending: input.attendingCount, maximum: input.maximumAttendees })
    : t('attendance.attending', { attending: input.attendingCount });

  if (input.status === 'cancelled') return t('attendance.cancelled', { spots });
  if (input.isFull) return t('attendance.full', { spots });
  if (input.isMinimumReached) return t('attendance.minimumReached', { spots });
  return t('attendance.needsMore', { spots, count: input.remainingMinimum });
}
